    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        int row = tabla_producto.getSelectedRow();
        int fila = tabla_producto.getRowCount();
        if(fila>0){
            String codigo = (String) model.getValueAt(row, 0); //model = tabla_producto
            Producto op = new Producto(codigo);
            oM.getTpr().Eliminar(op);
            model.removeRow(row); //elimino de la fila
            this.oM.getTpr().Guardar();
        }else{
            JOptionPane.showMessageDialog(null, "No hay producto seleccionados", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

