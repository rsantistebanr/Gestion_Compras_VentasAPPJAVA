    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        //limpio tabla
        int row = model.getRowCount();
        
        for(int i = row-1;i>=0;i--){
            model.removeRow(i);
        }
        
        String patron;
        patron = txt_barra.getText();
        List<Producto> lpro = oM.getTpr().Seleccionar(patron);

        for (int i = 0; i < lpro.size(); i++) {
            String[] columns = new String[4];
//            columns[0] = Integer.toString(i+1);
            columns[0] = lpro.get(i).getCodigo();
            columns[1] = lpro.get(i).getNombre();
            columns[2] = lpro.get(i).getUnidadMedida();
            columns[3] = Float.toString(lpro.get(i).getPrecioUnitario());

            model.addRow(columns);

        }

//        JOptionPane.showMessageDialog(null, patron, "Mensaje de bienvenida", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btn_buscarActionPerformed

