    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        int row = tabla_tu.getSelectedRow();
        int fila = tabla_tu.getRowCount();
        if(fila>0){
            String codigo = (String) model.getValueAt(row, 0); //model = tabla_producto
            entidades.TipoUnidadMedida tu = new entidades.TipoUnidadMedida(codigo);
            oM.getTtu().Eliminar(tu);
            model.removeRow(row); //elimino de la fila
            
        }else{
            JOptionPane.showMessageDialog(null, "No hay Tipo unidad seleccionadas", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

