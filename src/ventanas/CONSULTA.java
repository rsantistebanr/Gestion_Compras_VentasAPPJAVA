        model.addColumn("NÂ°");
        model.addColumn("Producto");
        model.addColumn("Stok");
        model.addColumn("U.Medida");
        model.addColumn("Compras");
        model.addColumn("Ventas");
        tabla_consulta.setModel(model);
        oM.getTpr().Consultar();
        
        
    
        lproductos = oM.getTpr().Seleccionar("");


        String[] columns = new String[6];

        for (int i = 0; i <lproductos.size() ; i++) {
            columns[0] = Integer.toString(i + 1);
            columns[1] = lproductos.get(i).getNombre();
            columns[2] = Float.toString(lproductos.get(i).getStock());
            columns[3] = lproductos.get(i).getUnidadMedida();
            columns[4] = Float.toString(lproductos.get(i).getcCompra());
            columns[5] = Float.toString(lproductos.get(i).getcVenta());
            model.addRow(columns);
        }
        
