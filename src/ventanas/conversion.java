    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        int fila = model.getRowCount();
        for (int i = fila - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        String tp = (String) combo_tu.getSelectedItem();
        List<Conversion> lcon = oM.getTco().Seleccionar(tp);

        for (int i = 0; i < lcon.size(); i++) {
            
            String[] columns = new String[4];
//            columns[0] = String.valueOf(i + 1);
            columns[0] = lcon.get(i).getCodigo();
            columns[1] = lcon.get(i).getDE();
            columns[2] = lcon.get(i).getA();
            columns[3] = Float.toString(lcon.get(i).getFactor());
            model.addRow(columns);
            
        }


    }//GEN-LAST:event_btn_buscarActionPerformed

