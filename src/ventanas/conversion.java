    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        int row = tabla_converisones.getSelectedRow();
        int fila = tabla_converisones.getRowCount();
        if(fila>0){
            String codigo = (String) model.getValueAt(row, 0); 
            Conversion oc = new Conversion(codigo);
            oM.getTco().Eliminar(oc);
            model.removeRow(row); //elimino de la fila
            oM.getTco().Guardar();
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un elemento.", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

