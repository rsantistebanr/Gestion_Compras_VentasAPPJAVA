    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
         UnidadMedida um = new UnidadMedida();
        int row = this.tabla_um.getSelectedRow(); //selecciono la fila que deseo eliminar

        if (row >= 0) {

 
            String codElimniar = (String) tabla_um.getValueAt(row, 0); //en ste caso es row->fila  y  1->la columna codigo
            um.setCodigo(codElimniar);
            this.oM.getTum().Eliminar(um);
            JOptionPane.showMessageDialog(this, "sizeum: "+ oM.getTum().size());
            model.removeRow(row); //elimino de la fila
            
            

        } else {
            JOptionPane.showMessageDialog(null, "No hay producto seleccionados", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

