    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        //limpiamos tabla
        int fila = model.getRowCount();
        for (int i = fila - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        String tu;
        tu = (String) combo_tpu.getSelectedItem();
        List<entidades.UnidadMedida> lum = oM.getTum().Seleccionar(tu);
        
        for (int i = 0; i < lum.size(); i++) {

            String[] columns = new String[2];
//            columns[0] = String.valueOf(i + 1); 
            columns[0] = lum.get(i).getCodigo();
            columns[1] = lum.get(i).getUnidadMedida();
            model.addRow(columns);
        }

    }//GEN-LAST:event_btn_buscarActionPerformed

